export function obtenerPreguntas(dia: number) {

  let preguntas: string[] = [];
  if (dia == 0) {
    preguntas = PREGUNTAS_BASICAS.slice(0, 3);
  } else if (dia >= 1 && dia <= 10) {
    const indiceInicial = (dia - 1) * 3;
    preguntas = PREGUNTAS_BASICAS.slice(indiceInicial, indiceInicial + 3);
  } else if (dia >= 11 && dia <= 20) {
    const indiceInicial = (dia - 11) * 3;
    preguntas = PREGUNTAS_INTERMEDIAS.slice(indiceInicial, indiceInicial + 3);
  } else if (dia >= 21 && dia <= 30) {
    const indiceInicial = (dia - 21) * 3;
    preguntas = PREGUNTAS_AVANZADAS.slice(indiceInicial, indiceInicial + 3);
  }

  return preguntas;
}